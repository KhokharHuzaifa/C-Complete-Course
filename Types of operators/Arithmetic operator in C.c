#include<stdio.h>
#include<conio.h>
int main(){
    // Arithmetic operators are used to perform common mathematical operations.
    int x = 12,y = 3;
    printf("%d", x + y);    // addition
    printf("%d", x - y);    //subtraction
    printf("%d", x * y);    // multiplication
    printf("%d", x / y);    // division
    printf("%d", x % y);    // modulus
    printf("%d", ++x);      // increment
    printf("%d", --x);      // decrement


}